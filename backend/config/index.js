const dotEnv = require("dotenv");

if (process.env.NODE_ENV !== "prod") {
  const configFile = `./.env.${process.env.NODE_ENV}`;
  dotEnv.config({ path: configFile });
} else {
  dotEnv.config();
}

module.exports = {
  DB_URL: process.env.MONGODB_URI,
  STRIPE_KEY: process.env.STRIPE_SECRET_KEY,
};
